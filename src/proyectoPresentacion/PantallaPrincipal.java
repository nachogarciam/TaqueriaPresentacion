                            datos[1] = lista.get(i).getCliente().getTelefono();
                            datos[2] = lista.get(i).getOrden();
                            datos[3] = lista.get(i).getFecha();
                            datos[4] = lista.get(i).isEntregado();

                            modelotabla1.addRow(datos);
